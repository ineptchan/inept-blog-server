create table tag_table
(
    id   bigint generated by default as identity not null,
    name varchar(24)                             not null,
    slug text not null,

    primary key (id),

    constraint uk_tag_name unique (name),
    constraint uk_tag_slug unique (slug)
);

create table categories_table
(
    id   bigint generated by default as identity not null,
    name varchar(24)                             not null,
    slug text not null,

    primary key (id),

    constraint uk_categories_name unique (name),
    constraint uk_categories_slug unique (slug)
);

create table article_table
(
    id             bigint generated by default as identity   not null,
    title          text                                      not null,
    slug           text                                      not null,
    featured_image text,
    content        text                                      not null,
    author_id      bigint                                    not null,
    category_id    bigint                                    not null,

    article_status text                                      not null,
    created_at     timestamp(6) with time zone default now() not null,
    updated_at     timestamp(6) with time zone,

    primary key (id),

    constraint uk_article_slug unique (slug),

    foreign key (author_id) references user_table (id),
    foreign key (category_id) references categories_table (id)
);

create table article_tag_table
(
    article_id bigint not null,
    tag_id     bigint not null,

    primary key (article_id, tag_id),

    foreign key (article_id) references article_table (id),
    foreign key (tag_id) references tag_table (id)
);

create table comment_table
(
    id         bigint generated by default as identity   not null,
    content    text                                      not null,
    status     text                                      not null,
    like_count bigint                                    not null,
    article_id bigint                                    not null,
    user_id    bigint                                    not null,
    parent_comment_id bigint,
    created_at timestamp(6) with time zone default now() not null,
    updated_at        timestamp(6) with time zone,
    deleted_at        timestamp(6) with time zone,

    primary key (id),
    foreign key (article_id) references article_table (id),
    foreign key (user_id) references user_table (id),
    foreign key (parent_comment_id) references comment_table (id)
);

create table comment_like_table
(
    id         bigint generated by default as identity   not null,
    comment_id bigint                                    not null,
    user_id    bigint                                    not null,
    created_at timestamp(6) with time zone default now() not null,

    primary key (id),

    constraint uk_comment_like_user_comment unique (comment_id, user_id),

    foreign key (comment_id) references comment_table (id),
    foreign key (user_id) references user_table (id)
);

create table object_storage_table
(
    id                bigint generated by default as identity   not null,
    owner_user_id     bigint                                    not null,
    owner_article_id  bigint                                    not null,
    purpose           text                                      not null,
    original_filename text                                      not null,
    content_type      text                                      not null,
    size_bytes        bigint                                    not null,
    sha256            text                                      not null,
    bucket            text                                      not null,
    object_key        text                                      not null,
    status            text                                      not null,
    visibility        text                                      not null,
    created_at        timestamp(6) with time zone default now() not null,
    updated_at        timestamp(6) with time zone,

    primary key (id),

    constraint uk_object_storage_object_key unique (object_key),
    foreign key (owner_user_id) references user_table (id),
    foreign key (owner_article_id) references article_table (id)
);