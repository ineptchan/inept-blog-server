create table permission_table
(
    id         bigint generated by default as identity   not null,
    code       varchar(64)                               not null,
    name       varchar(32)                               not null,
    description text,
    created_at timestamp(6) with time zone default now() not null,
    updated_at  timestamp(6) with time zone,

    primary key (id),

    constraint uk_permission_code unique (code)
);

create table role_table
(
    id         bigint generated by default as identity   not null,
    code       varchar(64)                               not null,
    name       varchar(32)                               not null,
    description text,
    created_at timestamp(6) with time zone default now() not null,
    updated_at  timestamp(6) with time zone,

    primary key (id),

    constraint uk_role_code unique (code)
);

create table role_permission_table
(
    role_id       bigint                                    not null,
    permission_id bigint                                    not null,
    created_at    timestamp(6) with time zone default now() not null,
    updated_at    timestamp(6) with time zone,

    primary key (role_id, permission_id),
    foreign key (role_id) references role_table (id),
    foreign key (permission_id) references permission_table (id)
);

create table user_table
(
    id         bigint generated by default as identity   not null,
    nickname   varchar(20)                               not null,
    avatar     text,
    username   varchar(20)                               not null,
    email      varchar(254),
    password   varchar(60)                               not null,
    status     boolean                                   not null,
    created_at timestamp(6) with time zone default now() not null,
    updated_at timestamp(6) with time zone,

    primary key (id),

    constraint uk_user_username unique (username),
    constraint uk_user_email unique (email)
);

create table user_role_table
(
    user_id    bigint                                    not null,
    role_id    bigint                                    not null,
    created_at timestamp(6) with time zone default now() not null,
    updated_at timestamp(6) with time zone,

    primary key (user_id, role_id),

    foreign key (user_id) references user_table (id),
    foreign key (role_id) references role_table (id)
);

create table refresh_token_table
(
    id         bigint generated by default as identity   not null,
    user_id    bigint                                    not null,
    token_hash varchar(64)                               not null,
    expires_at timestamp(6) with time zone               not null,
    created_at timestamp(6) with time zone default now() not null,
    updated_at     timestamp(6) with time zone,
    last_used_at   timestamp(6) with time zone,
    revoked_at     timestamp(6) with time zone,
    user_agent text,
    ip_address     varchar(45),
    replaced_by_id bigint,

    primary key (id),

    constraint uk_refresh_token_token_hash unique (token_hash),
    unique (replaced_by_id),

    foreign key (replaced_by_id) references refresh_token_table (id),
    foreign key (user_id) references user_table (id)
);


